package lesson_2;

import java.util.ArrayList;
import java.util.Map;

public class Variables {
	public static void main(String[] args) {
		// Для обьявления переменных можно не указывать тип
		// java компилятор сам понимает какой это тип
		var count = 15;

		// Плюсы var:
		// → Меньше бойлерплейта: var list = new ArrayList<String>();
		// → С длинными дженериками — просто спасение
		// → Легче рефакторить — меняешь правую часть, тип сам подхватывается
		// → Идеально для Stream API, Optional, try-with-resources, records
		// → В 2025 году — это уже стандарт де-факто в современном коде

		// Минусы / подводные камни:
		// → Тип не виден сразу — var user = service.findById(42); ← а что вернулось?
		// → Нельзя в публичных методах и полях (компилятор ругается)
		// → Новички и старые код-ревьюеры могут ныть
		// → В банках и корпорациях до сих пор запрещают стилем
		// → Ломает привычку «слева тип — справа реализация»

		// Вердикт котика: используй var везде, где тип и так очевиден.
		// Если читающий код поймёт тип по имени переменной или правой части — смело
		// пиши var.
		// Если есть сомнения — пиши тип явно. Мяу =^..^=
	}
}
